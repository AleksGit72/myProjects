#  –†–∞–∑–±–∏–µ–Ω–∏–µ –Ω–∞ —á–∞–Ω–∫–∏ üå∂Ô∏è
#  –ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞–µ—Ç—Å—è –¥–≤–µ —Å—Ç—Ä–æ–∫–∏: –Ω–∞ –æ–¥–Ω–æ–π ‚Äì —Å–∏–º–≤–æ–ª—ã, –Ω–∞ –¥—Ä—É–≥–æ–π ‚Äì —á–∏—Å–ª–æ n. –ò–∑ –ø–µ—Ä–≤–æ–π —Å—Ç—Ä–æ–∫–∏ —Ñ–æ—Ä–º–∏—Ä—É–µ—Ç—Å—è —Å–ø–∏—Å–æ–∫.
#  –ü—Ä–æ–≥—Ä–∞–º–º–∞ –Ω–∞ –±–∞–∑–µ —Ñ—É–Ω–∫—Ü–∏–∏ chunked() (–ø—Ä–∏–Ω–∏–º–∞–µ—Ç –Ω–∞ –≤—Ö–æ–¥ —Å–ø–∏—Å–æ–∫ –∏ —á–∏—Å–ª–æ, –∑–∞–¥–∞—é—â–µ–µ –¥–ª–∏–Ω—É —á–∞–Ω–∫–∞ (–∫—É—Å–∫–∞), –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç —Å–ø–∏—Å–æ–∫ –∏–∑ —á–∞–Ω–∫–æ–≤)
#  –≤—ã–≤–æ–¥–∏—Ç —Å–ø–∏—Å–æ–∫ —Å –≤–ª–æ–∂–µ–Ω–Ω—ã–º–∏ –ø–æ–¥—Å–ø–∏—Å–∫–∞–º–∏(—á–∞–Ω–∫–∞–º–∏) –¥–ª–∏–Ω–æ–π n 

def chunked(s, n):
    lst = []
    for i in range(0, len(s), n):
        lst.append(s[i:i + n])
    return lst

s, n = input().split(), int(input())
print(chunked(s, n))


